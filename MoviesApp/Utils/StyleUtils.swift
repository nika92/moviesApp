import UIKit

class StyleUtils {
    
    static func appMainColor () -> UIColor {
        return UIColor(red: CGFloat(35.0/255.0), green: CGFloat(119.0/255.0), blue: CGFloat(189.0/255.0), alpha: CGFloat(1.0))
    }
    
    static func appMainColorWithAlpha (alpha: CGFloat) -> UIColor {
        return UIColor(red: CGFloat(35.0/255.0), green: CGFloat(119.0/255.0), blue: CGFloat(189.0/255.0), alpha: CGFloat(alpha))
    }

}
